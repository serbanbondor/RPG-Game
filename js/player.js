class Player {
    constructor(classType, health, energy, mana, focus, strength, agility, intellect, defence) {
        this.classType = classType;
        this.health = health;
        this.energy = energy;
        this.mana = mana;
        this.focus = focus;
        this.strength = strength;
        this.agility = agility;
        this.intellect = intellect;
        this.defence = defence;
    }
}